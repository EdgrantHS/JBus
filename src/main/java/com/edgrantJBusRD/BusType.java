package com.edgrantJBusRD;

/**
 * The BusType enum represents different types of buses in the transportation system.
 * Each enum value corresponds to a specific type of bus.
 */
public enum BusType {

    /**
     * Represents a regular bus type.
     */
    REGULAR,

    /**
     * Represents a high-decker bus type.
     */
    HIGH_DECKER,

    /**
     * Represents a minibus type.
     */
    MINIBUS,

    /**
     * Represents a double-decker bus type.
     */
    DOUBLE_DECKER
}
